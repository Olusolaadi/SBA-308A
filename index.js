// Loop over the object 'currencies' and add 'option' element
function listCurr(options) {
  return Object.entries(options)
    .map(([currencyKeys, currencyCode]) =>
        `<option value="${currencyKeys}">${currencyKeys} - ${currencyCode}</option>`)
    .join("");
}

